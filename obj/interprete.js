class Interprete
{
    #nombre;
    #apellido;
    #banda;

    get nombre()
    {
        return this.#nombre;
    }
    get apellido()
    {
        return this.#apellido;
    }
    get banda()
    {
        return this.#banda;
    }
    set nombre(nombre)
    {
        this.#nombre = nombre;
    }
    set apellido(apellido)
    {
        this.#apellido = this.apellido;
    }
    set banda(banda)
    {
        this.#banda = banda;
    }
}

